    rc.name AS 'Категория номера',
    COUNT(*) AS 'Количество проживающих'
FROM room_in_booking rib
JOIN room r ON rib.id_room = r.id_room
JOIN hotel h ON r.id_hotel = h.id_hotel
JOIN room_category rc ON r.id_room_category = rc.id_room_category
WHERE h.name = 'Космос'
    AND '2019-03-23' >= rib.checkin_date 
    AND '2019-03-23' < rib.checkout_date
GROUP BY rc.id_room_category, rc.name
ORDER BY rc.name;

