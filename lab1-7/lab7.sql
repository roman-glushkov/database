CREATE VIEW informatics_marks AS
SELECT 
    s.name AS student,
    sub.name AS subject,
    m.mark
FROM student s
JOIN mark m ON s.id_student = m.id_student
JOIN lesson l ON m.id_lesson = l.id_lesson
JOIN subject sub ON l.id_subject = sub.id_subject
WHERE sub.name = 'Информатика'
ORDER BY m.mark DESC;

