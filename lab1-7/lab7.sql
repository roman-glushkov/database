-- который ведется в группе. Оформить в виде процедуры, на входе идентификатор группы.

DELIMITER //
CREATE PROCEDURE get_debtors(IN group_id INT)
BEGIN
    SELECT 
        s.name AS student_name,
        GROUP_CONCAT(sub.name ORDER BY sub.name SEPARATOR ', ') AS debt_subjects
    FROM student s
    JOIN (
        SELECT DISTINCT id_subject 
        FROM lesson 
        WHERE id_group = group_id
    ) AS group_subjects
    JOIN subject sub ON group_subjects.id_subject = sub.id_subject
    WHERE s.id_group = group_id
    AND NOT EXISTS (
        SELECT 1
        FROM mark m
        JOIN lesson l ON m.id_lesson = l.id_lesson
        WHERE m.id_student = s.id_student
        AND l.id_subject = group_subjects.id_subject
        AND l.id_group = group_id
    )
    GROUP BY s.id_student, s.name
    HAVING debt_subjects IS NOT NULL
    ORDER BY s.name;
END//
